// Variables


// Variables are containers for storing data values. In JavaScript, we can declare variables using:

// var – (old, mostly avoided now)

// let – modern, block-scoped

// const – modern, block-scoped & cannot be reassigned

let name = "Prafull";
let age = 25;

console.log(name); // Prafull
console.log(age);

const a = "Test";
console.log(a);





